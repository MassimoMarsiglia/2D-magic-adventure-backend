package com.example.demo;

public class TestEntry {

    String test;
    int test2;
    public TestEntry(String test, int test2) {
        this.test = test;
        this.test2 = test2;
    }


    public String getTest() {
        return this.test;
    }

    public void setTest(String test) {
        this.test = test;
    }

    public int getTest2() {
        return this.test2;
    }

    public void setTest2(int test2) {
        this.test2 = test2;
    }

    
}
